// Student score
let score = 85;

// Switch with true for range checking
switch (true) {
    case (score >= 90):
        console.log("Grade: A");
        break;
    case (score >= 75):
        console.log("Grade: B");
        break;
    case (score >= 50):
        console.log("Grade: C");
        break;
    default:
        console.log("Grade: Fail");
}